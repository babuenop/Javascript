var numero =1;
// Swithc esta hecho para validar casos 
switch (numero){
    case 1: 
        console.log("soy uno");
        break;
    case 10:
        console.log("soy un 10");
        break
    case 100:
        console.log("soy un 100");
        break
    //Default funciona como la parte negativa del IF
    default: 
        console.log("No soy nada");
}